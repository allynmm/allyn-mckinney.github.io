function fizzBuzz() {
  var count = 1;
  while (count <= 100) {
   if (count % 3 === 0 && count % 5 === 0) {
    console.log('fizzbuzz');
   } else if (count % 3 === 0) {
    console.log('fizz');
   } else if (count % 5 === 0) {
    console.log('buzz');
   } else {
    console.log(count);
   }
   count++;
  }
}
////////////////////////////////////////////////////////////////////////////////
// drawChessboard //////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function drawChessboard(x) { //x represents rows and columns
  var oddPiece = '# ';
  var evenPiece = ' #';
  var oddRow = oddPiece.repeat(x);
  var evenRow = evenPiece.repeat(x);
  var output = '';
  // use the array to create columns
  for (var i = 1; i <= x; i++) {
    //even columns
    if (i % 2 === 0 && i < x) {
      output += evenRow + '\n';
      //odd columns
    } else if (i % 2 !== 0 && i < x) {
      output += oddRow + '\n';
    } else if (i % 2 === 0 && i === x) {
      output += evenRow;
    } else if (i % 2 !== 0 && i === x) {
      output += oddRow;
    }
  }
  return output;
}

