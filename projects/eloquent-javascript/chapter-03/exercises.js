function isEven(num) {
  var booFlagVal = false;
  if (num % 2 === 0){
    booFlagVal = true;
  }
  return booFlagVal;
}

////////////////////////////////////////////////////////////////////////////////
// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countChars(string, char) {
  var charCount = 0;
  for (let i = 0; i < string.length; i++){
    if(string[i].toLowerCase() === char.toLowerCase()){
      charCount++;
    }
  }
  return charCount;
}

////////////////////////////////////////////////////////////////////////////////
// countBs /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countBs(string) {
  var bCount = 0;
  for (let i = 0; i < string.length; i++){
    if (string[i].toUpperCase() === 'B') {
      bCount++;
    }
  }
  return bCount;
}

