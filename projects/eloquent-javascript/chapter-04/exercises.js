// range ///////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function range(start, end, step=1) {
  var arr = [];
  var count = start;
  if (start === end){
    return arr;
  } else if (start < end && step < start){
    return arr;
  } else if (start > end && step > end){
    return arr;
  } else if (start > end) {
    // let count = start;
    while (count >= end){
      arr.push(count);
      count-= step;
    }
  } else if (start < end) {
    // let count = start;
    while (count <= end){
      arr.push(count);
      count+= step;
    }
  } else if (start < end && step < start) {
    return [];
  } else if (start > end && step > start) {
    return [];
  }
  
  return arr;
}

