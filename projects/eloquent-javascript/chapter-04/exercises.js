// arrayToList /////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function arrayToList(array) {
 let rest = null;

 for (let i = array.length -1; i >= 0; i--){
  rest = { value: array[i], rest: rest };
 }

 return rest;
}

////////////////////////////////////////////////////////////////////////////////
// listToArray /////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function listToArray(list, arr=[]) {
 // base
 if (list.rest === null){
  arr.push(list.value);
  return arr;
 }

 // recursion
 arr.push(list.value);
 return listToArray(list.rest , arr);
}

////////////////////////////////////////////////////////////////////////////////
// prepend /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function prepend(element, list) {
 var output = {};
 output.value = element;
 output.rest = Object.assign(list);

 return output;
}

////////////////////////////////////////////////////////////////////////////////
// nth /////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function nth(list, n) {
  // base
  if (n === 0){
    return list.value;
  } else if (n < 0) {
    return undefined;
  }

  // recursion

  return nth(list.rest, n - 1);
}

