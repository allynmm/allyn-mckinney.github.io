/** 
CONTROL FLOW

0. Conrtrol flow can be described as decisions our code makes based on the results of true or false questions (think of a flow chart) which can be achieved by conditional statements
*/

// 1. IF
// If tells JavaScript to execute the code in the curly braces under certain circumstances defined in the parentheses
  // These circumstances are Boolean conditions and may only be true or false

// If conditions are true → code is executed. If conditions are false → code is not executed

// 2. ELSE
// Provides alternate code to execute if conditions within an if statement are false

// 3. ELSE IF
// If you have multiple conditions that need to be addressed, you can chain if statements together with else if statements.

  // Order matters in if and else if statements. Function is executed from top to bottom so be mindful of what comes first